package Metier;

public class Point extends Affichable
{
	int x;
	int y;

	
	//Constructeur
	public Point(int x, int y)
	{
		
	}
	
	public void Delete()
	{
		
	}
	
	//Getteur
	public int getX()
	{
		return this.x;
	}
	
	public int getY()
	{
		return this.y;
	}
	
	//Setteur
	public void setX(int x)
	{
		this.x = x;
	}
	
	public void setY(int y)
	{
		this.y = y;
	}
	
}
